// Given 'pgdir', a pointer to a page directory, pgdir_walk returns
// a pointer to the page table entry (PTE) for linear address 'va'.
// This requires walking the two-level page table structure.
//
// The relevant page table page might not exist yet.
// If this is true, and create == false, then pgdir_walk returns NULL.
// Otherwise, pgdir_walk allocates a new page table page with page_alloc.
//    - If the allocation fails, pgdir_walk returns NULL.
//    - Otherwise, the new page's reference count is incremented,
//	the page is cleared,
//	and pgdir_walk returns a pointer into the new page table page.
//
// Hint 1: you can turn a PageInfo * into the physical address of the
// page it refers to with page2pa() from kern/pmap.h.
//
// Hint 2: the x86 MMU checks permission bits in both the page directory
// and the page table, so it's safe to leave permissions in the page
// directory more permissive than strictly necessary.
//
// Hint 3: look at inc/mmu.h for useful macros that mainipulate page
// table and page directory entries.
//

pte_t *
pgdir_walk(pde_t *pgdir, const void *va, int create)
{
	// Fill this function in
	pde_t *ent = pgdir + PDX(va);
	//cprintf("walk *ent %p\n", *ent);
	if(*ent & PTE_P)
		return KADDR((physaddr_t) (PTX(va) + (pte_t *) PTE_ADDR(*ent)));
	else if(create)
	{
		struct PageInfo *pi = inited ? pte_page_alloc() : page_alloc(ALLOC_ZERO);
		if(!pi)
			return NULL;
		*ent = page2pa(pi) | PTE_P | PTE_W | PTE_U;
		++pi->pp_ref;
		return KADDR((physaddr_t) (PTX(va) + (pte_t *) PTE_ADDR(*ent)));
	}
	else
		return NULL;
}

//
// Map [va, va+size) of virtual address space to physical [pa, pa+size)
// in the page table rooted at pgdir.  Size is a multiple of PGSIZE, and
// va and pa are both page-aligned.
// Use permission bits perm|PTE_P for the entries.
//
// This function is only intended to set up the ``static'' mappings
// above UTOP. As such, it should *not* change the pp_ref field on the
// mapped pages.
//
// Hint: the TA solution uses pgdir_walk
void
boot_map_region(pde_t *pgdir, uintptr_t va, size_t size, physaddr_t pa, int perm)
{
	// Fill this function in
	for(size_t offset = 0; offset != size; offset += PGSIZE)
	{
		// if(inited) cprintf("map va %p to pa %p\n", va, pa);
		volatile pte_t *pte = pgdir_walk(pgdir, (const void *) (va + offset), 1);
		if(!pte)
			panic("map failed\n");
		*pte = (pa + offset) | perm | PTE_P;
		// if(inited) cprintf("*pte = %08x\n", *pte);
	}
}

