// Requirements
//   - If there is already a page mapped at 'va', it should be page_remove()d.
//   - If necessary, on demand, a page table should be allocated and inserted
//     into 'pgdir'.
//   - pp->pp_ref should be incremented if the insertion succeeds.
//   - The TLB must be invalidated if a page was formerly present at 'va'.
//
// Corner-case hint: Make sure to consider what happens when the same
// pp is re-inserted at the same virtual address in the same pgdir.
// However, try not to distinguish this case in your code, as this
// frequently leads to subtle bugs; there's an elegant way to handle
// everything in one code path.
//
// RETURNS:
//   0 on success
//   -E_NO_MEM, if page table couldn't be allocated
//
// Hint: The TA solution is implemented using pgdir_walk, page_remove,
// and page2pa.
//
int
page_insert(pde_t *pgdir, struct PageInfo *pp, void *va, int perm)
{
	// Fill this function in
	tlb_invalidate(pgdir, va);
	pte_t *pte = pgdir_walk(pgdir, va, 0);
	bool remove = pte && (*pte & PTE_P);
	//bool spec = (pp->pp_ref == 1);
	if(remove)
	{
		//if(PTE_ADDR(*pte) == page2pa(pp))
		//	--pp->pp_ref;
		//else
		bool spec = (pp->pp_ref == 1 && PTE_ADDR(*pte) == page2pa(pp));
		page_remove(pgdir, va);
		//tlb_invalidate(pgdir, va);
		if(spec)
		{
			struct PageInfo *tmp = page_alloc_spec(0, pp);
			//cprintf("aha %p\n", tmp);
			assert(tmp == pp);
		}
	}
	pte = pgdir_walk(pgdir, va, 1);
	if(!pte)
		return -E_NO_MEM;
	*pte = page2pa(pp) | perm | PTE_P;
	++pp->pp_ref;
	return 0;
}

//
// Return the page mapped at virtual address 'va'.
// If pte_store is not zero, then we store in it the address
// of the pte for this page.  This is used by page_remove and
// can be used to verify page permissions for syscall arguments,
// but should not be used by most callers.
//
// Return NULL if there is no page mapped at va.
//
// Hint: the TA solution uses pgdir_walk and pa2page.
//
struct PageInfo *
page_lookup(pde_t *pgdir, void *va, pte_t **pte_store)
{
	// Fill this function in
	pte_t *pte = pgdir_walk(pgdir, va, 0);
	if(pte_store)
		*pte_store = pte;
	if(!pte || !(*pte & PTE_P))
		return NULL;
	if (PGNUM(PTE_ADDR(*pte)) < npages) {  // ONLY return valid pages, while others are not ref-counted
		return pa2page(PTE_ADDR(*pte));
	} else {
		return NULL;
	}
}

//
// Unmaps the physical page at virtual address 'va'.
// If there is no physical page at that address, silently does nothing.
//
// Details:
//   - The ref count on the physical page should decrement.
//   - The physical page should be freed if the refcount reaches 0.
//   - The pg table entry corresponding to 'va' should be set to 0.
//     (if such a PTE exists)
//   - The TLB must be invalidated if you remove an entry from
//     the page table.
//
// Hint: The TA solution is implemented using page_lookup,
// 	tlb_invalidate, and page_decref.
//
void
page_remove(pde_t *pgdir, void *va)
{
	tlb_invalidate(pgdir, va);
	// Fill this function in
	pte_t *pte;
	struct PageInfo *pi = page_lookup(pgdir, va, &pte);
	if(!pi)
		return;
	page_decref(pi);
	tlb_invalidate(pgdir, va);
	*pte = 0;
}

