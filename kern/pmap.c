//
// Allocates a physical page.  If (alloc_flags & ALLOC_ZERO), fills the entire
// returned physical page with '\0' bytes.  Does NOT increment the reference
// count of the page - the caller must do these if necessary (either explicitly
// or via page_insert).
//
// Be sure to set the pp_link field of the allocated page to NULL so
// page_free can check for double-free bugs.
//
// Returns NULL if out of free memory.
//
// Hint: use page2kva and memset
struct PageInfo *
pte_page_alloc(void)
{
	int pp_begin = (EXTPHYSMEM + (32 << 20)) / PGSIZE;
	int pp_end   = (EXTPHYSMEM + (64 << 20)) / PGSIZE;
	for(int i = pp_begin; i < pp_end; ++i)
		if(pages[i].pp_ref == 233)
		{
			// cprintf("pte alloc %p\n", i * PGSIZE);
			struct PageInfo *ret = pages + i;
			// cprintf("gg %p\n", page2kva(ret));
			memset(page2kva(ret), 0, PGSIZE);
			// cprintf("pte asdf %p\n", i * PGSIZE);
			return ret;
		}
	return NULL;
}

static struct PageInfo * segtree_alloc_page_left() {
	int id = segtree_alloc_left();
	if (id == -1) {
		return NULL;
	}
	return pages + id;
}

static struct PageInfo * segtree_alloc_page_right() {
	int id = segtree_alloc_right();
	if (id == -1) {
		return NULL;
	}
	return pages + id;
}

static struct PageInfo * segtree_alloc_page_spec(struct PageInfo *pp) {
	int id = pp - pages;
	if (segtree_get(id) == 1) {
		return NULL;
	}
	segtree_modify(id, 1);
	return pp;
}

static void segtree_free_page(struct PageInfo *pp) {
	int id = pp - pages;
	segtree_free(id);
}

